import React, { useState } from "react";
import { useTranslation } from "react-i18next";
import { useNavigate } from "react-router-dom";
import {
  FaGraduationCap,
  FaBriefcase,
  FaStar,
  FaFileAlt,
  FaCheck,
} from "react-icons/fa";
import axios from "axios";

const MentorApplicationForm: React.FC = () => {
  const { t } = useTranslation();
  const navigate = useNavigate();
  const [form, setForm] = useState({
    specialization: "",
    experience: "",
    education: "",
    introduction: "",
    hourlyRate: "",
    languages: "",
    certifications: "",
    portfolio: "",
    availability: "",
    motivation: "",
  });
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);
  const [success, setSuccess] = useState(false);

  const handleChange = (
    e: React.ChangeEvent<
      HTMLInputElement | HTMLTextAreaElement | HTMLSelectElement
    >
  ) => {
    setForm({ ...form, [e.target.name]: e.target.value });
  };

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    setLoading(true);
    setError(null);
    setSuccess(false);

    try {
      const token = localStorage.getItem("token");
      await axios.post(
        "https://ezkv3-production.up.railway.app/api/mentor/apply",
        form,
        {
          headers: {
            Authorization: `Bearer ${token}`,
          },
        }
      );
      setLoading(false);
      setSuccess(true);
      setTimeout(() => navigate("/dashboard"), 2000);
    } catch (err: any) {
      setLoading(false);
      if (err.response && err.response.data && err.response.data.message) {
        setError(err.response.data.message);
      } else {
        setError(t("applicationError") || "Ïã†Ï≤≠Ïóê Ïã§Ìå®ÌñàÏäµÎãàÎã§.");
      }
    }
  };

  const specializations = [
    "JavaScript/TypeScript",
    "React/Vue/Angular",
    "Node.js/Express",
    "Python/Django/Flask",
    "Java/Spring",
    "C#/.NET",
    "Mobile Development",
    "DevOps/AWS",
    "Database/SQL",
    "Machine Learning/AI",
    "UI/UX Design",
    "Project Management",
  ];

  const experienceLevels = ["1-2 years", "3-5 years", "5-8 years", "8+ years"];

  if (success) {
    return (
      <div className="min-h-screen bg-gray-50 dark:bg-gray-900 flex items-center justify-center">
        <div className="max-w-md w-full bg-white dark:bg-gray-800 rounded-2xl shadow-xl p-8 text-center">
          <div className="w-16 h-16 bg-green-100 dark:bg-green-900 rounded-full flex items-center justify-center mx-auto mb-4">
            <FaCheck className="text-green-600 dark:text-green-400 text-2xl" />
          </div>
          <h2 className="text-2xl font-bold text-gray-900 dark:text-white mb-2">
            {t("applicationSubmitted")}
          </h2>
          <p className="text-gray-600 dark:text-gray-400 mb-4">
            {t("applicationSubmittedMessage")}
          </p>
          <div className="animate-pulse">
            <div className="w-full bg-gray-200 dark:bg-gray-700 rounded-full h-2">
              <div
                className="bg-blue-600 h-2 rounded-full"
                style={{ width: "60%" }}
              ></div>
            </div>
            <p className="text-sm text-gray-500 dark:text-gray-400 mt-2">
              {t("reviewInProgress")}
            </p>
          </div>
        </div>
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-gray-50 dark:bg-gray-900 py-8">
      <div className="container mx-auto px-4 max-w-4xl">
        {/* Ìó§Îçî */}
        <div className="text-center mb-8">
          <div className="w-20 h-20 bg-blue-100 dark:bg-blue-900 rounded-full flex items-center justify-center mx-auto mb-4">
            <FaGraduationCap className="text-blue-600 dark:text-blue-400 text-3xl" />
          </div>
          <h1 className="text-3xl font-bold text-gray-900 dark:text-white mb-2">
            {t("becomeMentor")}
          </h1>
          <p className="text-gray-600 dark:text-gray-400 max-w-2xl mx-auto">
            {t("mentorApplicationDescription")}
          </p>
        </div>

        {/* ÏßÑÌñâ Îã®Í≥Ñ */}
        <div className="bg-white dark:bg-gray-800 rounded-xl shadow-sm p-6 mb-8 border border-gray-200 dark:border-gray-700">
          <div className="flex items-center justify-between mb-6">
            <div className="flex items-center gap-4">
              <div className="w-8 h-8 bg-blue-600 text-white rounded-full flex items-center justify-center text-sm font-semibold">
                1
              </div>
              <span className="text-gray-900 dark:text-white font-medium">
                {t("applicationForm")}
              </span>
            </div>
            <div className="flex items-center gap-4">
              <div className="w-8 h-8 bg-gray-200 dark:bg-gray-700 text-gray-500 rounded-full flex items-center justify-center text-sm">
                2
              </div>
              <span className="text-gray-500 dark:text-gray-400">
                {t("adminReview")}
              </span>
            </div>
            <div className="flex items-center gap-4">
              <div className="w-8 h-8 bg-gray-200 dark:bg-gray-700 text-gray-500 rounded-full flex items-center justify-center text-sm">
                3
              </div>
              <span className="text-gray-500 dark:text-gray-400">
                {t("approval")}
              </span>
            </div>
          </div>
        </div>

        {/* Ïã†Ï≤≠ Ìèº */}
        <div className="bg-white dark:bg-gray-800 rounded-xl shadow-sm p-8 border border-gray-200 dark:border-gray-700">
          <form onSubmit={handleSubmit} className="space-y-6">
            {/* Ï†ÑÎ¨∏ Î∂ÑÏïº */}
            <div>
              <label className="block text-sm font-medium text-gray-700 dark:text-gray-300 mb-2">
                <FaStar className="inline mr-2 text-blue-600" />
                {t("specialization")} *
              </label>
              <select
                name="specialization"
                value={form.specialization}
                onChange={handleChange}
                required
                className="w-full px-4 py-3 border border-gray-300 dark:border-gray-600 rounded-lg bg-white dark:bg-gray-700 text-gray-900 dark:text-white focus:ring-2 focus:ring-blue-500 focus:border-transparent"
              >
                <option value="">{t("selectSpecialization")}</option>
                {specializations.map((spec) => (
                  <option key={spec} value={spec}>
                    {spec}
                  </option>
                ))}
              </select>
            </div>

            {/* Í≤ΩÎ†• */}
            <div>
              <label className="block text-sm font-medium text-gray-700 dark:text-gray-300 mb-2">
                <FaBriefcase className="inline mr-2 text-blue-600" />
                {t("experience")} *
              </label>
              <select
                name="experience"
                value={form.experience}
                onChange={handleChange}
                required
                className="w-full px-4 py-3 border border-gray-300 dark:border-gray-600 rounded-lg bg-white dark:bg-gray-700 text-gray-900 dark:text-white focus:ring-2 focus:ring-blue-500 focus:border-transparent"
              >
                <option value="">{t("selectExperience")}</option>
                {experienceLevels.map((level) => (
                  <option key={level} value={level}>
                    {level}
                  </option>
                ))}
              </select>
            </div>

            {/* ÌïôÎ†• */}
            <div>
              <label className="block text-sm font-medium text-gray-700 dark:text-gray-300 mb-2">
                <FaGraduationCap className="inline mr-2 text-blue-600" />
                {t("education")}
              </label>
              <textarea
                name="education"
                value={form.education}
                onChange={handleChange}
                rows={3}
                placeholder={t("educationPlaceholder")}
                className="w-full px-4 py-3 border border-gray-300 dark:border-gray-600 rounded-lg bg-white dark:bg-gray-700 text-gray-900 dark:text-white focus:ring-2 focus:ring-blue-500 focus:border-transparent resize-none"
              />
            </div>

            {/* ÏûêÍ∏∞ÏÜåÍ∞ú */}
            <div>
              <label className="block text-sm font-medium text-gray-700 dark:text-gray-300 mb-2">
                <FaFileAlt className="inline mr-2 text-blue-600" />
                {t("introduction")} *
              </label>
              <textarea
                name="introduction"
                value={form.introduction}
                onChange={handleChange}
                rows={4}
                placeholder={t("introductionPlaceholder")}
                required
                className="w-full px-4 py-3 border border-gray-300 dark:border-gray-600 rounded-lg bg-white dark:bg-gray-700 text-gray-900 dark:text-white focus:ring-2 focus:ring-blue-500 focus:border-transparent resize-none"
              />
            </div>

            {/* ÏãúÍ∏â */}
            <div>
              <label className="block text-sm font-medium text-gray-700 dark:text-gray-300 mb-2">
                üí∞ {t("hourlyRate")} (USD) *
              </label>
              <input
                type="number"
                name="hourlyRate"
                value={form.hourlyRate}
                onChange={handleChange}
                placeholder="25"
                min="10"
                max="200"
                required
                className="w-full px-4 py-3 border border-gray-300 dark:border-gray-600 rounded-lg bg-white dark:bg-gray-700 text-gray-900 dark:text-white focus:ring-2 focus:ring-blue-500 focus:border-transparent"
              />
            </div>

            {/* ÏÇ¨Ïö© Ïñ∏Ïñ¥ */}
            <div>
              <label className="block text-sm font-medium text-gray-700 dark:text-gray-300 mb-2">
                üåç {t("languages")} *
              </label>
              <input
                type="text"
                name="languages"
                value={form.languages}
                onChange={handleChange}
                placeholder={t("languagesPlaceholder")}
                required
                className="w-full px-4 py-3 border border-gray-300 dark:border-gray-600 rounded-lg bg-white dark:bg-gray-700 text-gray-900 dark:text-white focus:ring-2 focus:ring-blue-500 focus:border-transparent"
              />
            </div>

            {/* ÏûêÍ≤©Ï¶ù */}
            <div>
              <label className="block text-sm font-medium text-gray-700 dark:text-gray-300 mb-2">
                üèÜ {t("certifications")}
              </label>
              <textarea
                name="certifications"
                value={form.certifications}
                onChange={handleChange}
                rows={2}
                placeholder={t("certificationsPlaceholder")}
                className="w-full px-4 py-3 border border-gray-300 dark:border-gray-600 rounded-lg bg-white dark:bg-gray-700 text-gray-900 dark:text-white focus:ring-2 focus:ring-blue-500 focus:border-transparent resize-none"
              />
            </div>

            {/* Ìè¨Ìä∏Ìè¥Î¶¨Ïò§ */}
            <div>
              <label className="block text-sm font-medium text-gray-700 dark:text-gray-300 mb-2">
                üîó {t("portfolio")}
              </label>
              <input
                type="url"
                name="portfolio"
                value={form.portfolio}
                onChange={handleChange}
                placeholder="https://github.com/yourusername"
                className="w-full px-4 py-3 border border-gray-300 dark:border-gray-600 rounded-lg bg-white dark:bg-gray-700 text-gray-900 dark:text-white focus:ring-2 focus:ring-blue-500 focus:border-transparent"
              />
            </div>

            {/* Î©òÌÜ†ÎßÅ ÎèôÍ∏∞ */}
            <div>
              <label className="block text-sm font-medium text-gray-700 dark:text-gray-300 mb-2">
                üí≠ {t("motivation")} *
              </label>
              <textarea
                name="motivation"
                value={form.motivation}
                onChange={handleChange}
                rows={3}
                placeholder={t("motivationPlaceholder")}
                required
                className="w-full px-4 py-3 border border-gray-300 dark:border-gray-600 rounded-lg bg-white dark:bg-gray-700 text-gray-900 dark:text-white focus:ring-2 focus:ring-blue-500 focus:border-transparent resize-none"
              />
            </div>

            {/* Ï†úÏ∂ú Î≤ÑÌäº */}
            <div className="flex gap-4 pt-6">
              <button
                type="button"
                onClick={() => navigate("/dashboard")}
                className="flex-1 px-6 py-3 border border-gray-300 dark:border-gray-600 text-gray-700 dark:text-gray-300 rounded-lg hover:bg-gray-50 dark:hover:bg-gray-700 transition-colors"
              >
                {t("cancel")}
              </button>
              <button
                type="submit"
                disabled={loading}
                className="flex-1 px-6 py-3 bg-blue-600 text-white rounded-lg hover:bg-blue-700 disabled:opacity-50 disabled:cursor-not-allowed transition-colors"
              >
                {loading ? t("submitting") : t("submitApplication")}
              </button>
            </div>

            {/* ÏóêÎü¨ Î©îÏãúÏßÄ */}
            {error && (
              <div className="text-red-500 text-sm text-center mt-4">
                {error}
              </div>
            )}
          </form>
        </div>
      </div>
    </div>
  );
};

export default MentorApplicationForm;
